    public String buscarArchivo() {
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de Texto", "txt");
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(filter);
        File ruta = new File("e:/carpeta/");
        fileChooser.setCurrentDirectory(ruta);
        int result = fileChooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            String dir = String.valueOf(file).replace("\\", "//");
            return dir;
        }
        return "";
    }
    
    private void guardarArchivo(String contenido, String archivo){
        try {  
            FileWriter fw = new FileWriter(archivo);
            BufferedWriter bw = new BufferedWriter(fw);

            bw.write(contenido);

            bw.close();
            fw.close();
             
        } catch (Exception e){
           
        }
    }
    
    private static String leerArchivo(String archivo){
        StringBuilder contenido = new StringBuilder();
        try {
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            String linea;

            while ((linea = br.readLine()) != null) {
                contenido.append(linea).append("\n");
            }

            br.close();
            fr.close();
        } catch (Exception e) {
            
        }
        return contenido.toString();
    }
    
    public String generarNombre(){ 
        Random random = new Random();
        int num = random.nextInt(150);
        String nombre = "resumen" + num;
        return nombre;
    }

